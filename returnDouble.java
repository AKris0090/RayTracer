package Textures;

public class returnDouble {

    double u;
    double v;

    public double getU() {
        return u;
    }

    public void setU(double u) {
        this.u = u;
    }

    public double getV() {
        return v;
    }

    public void setV(double v) {
        this.v = v;
    }

    public returnDouble(double u, double v){
        this.u = u;
        this.v = v;
    }
}
